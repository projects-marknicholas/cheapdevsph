function showPostImage() {
	var element = document.getElementById('post-modal');

	if (element.style.display === "block") {
		element.style.display = "none";
	}
	else{
		element.style.display = "block";
	}
}

function showPostImages() {
	var element = document.getElementById('post-modal-text');

	if (element.style.display === "block") {
		element.style.display = "none";
	}
	else{
		element.style.display = "block";
	}
}